p= input('Enter first number: ')  
n = input('Enter second number: ')  

sum = float(p) + float(n)  
  
min = float(p) - float(n)  
 
mul = float(p) * float(n)  

div = float(p) / float(n)  
 
print('The sum of {0} and {1} is {2}'.format(p, n, sum))  
  
print('The subtraction of {0} and {1} is {2}'.format(p, n, min))  

print('The multiplication of {0} and {1} is {2}'.format(p, n, mul))  

print('The division of {0} and {1} is {2}'.format(p, n, div))  
